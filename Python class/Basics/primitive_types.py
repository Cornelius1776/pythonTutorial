# primitive datatypes are types built-in directly into a programming language, from which other data
# types can be created. The different types of primitive datatypes in python are as follows

message = "Hello World"  # string
age = 25  # int
rate = 2.4  # floats
is_rich = True  # boolean

print(message)
print(age)
print(rate)
print(is_rich)

# the datatype can be checked using the type() method
print(type(message))
print(type(age))
print(type(rate))
print(type(is_rich))

a = type(message)
print(a)
