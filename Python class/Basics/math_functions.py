import math

pi = 3.142
# round() cuts off the decimal points
print(round(pi))

# floor() does the same available from the math module
print(math.floor(pi))

# ceil() takes it to the nearest whole number
print(math.ceil(pi))

num = -340
# abs() gives the absolute value(how far a number is from zero) of a number
print(abs(num))

# pow() raises a number to the power of the second argument e.g (3, 4) 3 to the power of 4
print(pow(pi, 5))

# returns the square root of the number
print(math.sqrt(100))
